/**Clase para poner editable una columna del
     * TableSorter*/
    private static class MyTableSorter extends TableSorter
    {
     @Override
     public boolean isCellEditable(int row, int column)
     {
      return false;
     }
    }

    public JScrollPane crearTabla()
    {
        sorter = new MyTableSorter();
        Table = new JTable(sorter);
        Table.setDefaultRenderer(Object.class, new TablaFilasColoresRender());
        Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
       // sorter.addMouseListenerToHeaderInTable(Table);
        JScrollPane scrollpane = new JScrollPane(Table);
        scrollpane.setVerticalScrollBarPolicy(javax.swing.ScrollPaneConstants.VERTICAL_SCROLLBAR_ALWAYS);
        return scrollpane;
    }

    public void ActualizarTabla(String query)
    {
     db.executeQuery2(query);
     Table.getColumnModel().getColumn(0).setCellRenderer(new TablaFilasColoresRender());
     Table.getColumnModel().getColumn(3).setCellRenderer(new TablaFilasColoresRender());
    }

