    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) 
    {
    	if(jRBAnchoFijo.isSelected())
    	{
    	 if(!jTFLeft.getText().equals("") && !jTFRight.getText().equals(""))
    	 {	 
    	  double l = Double.parseDouble(jTFLeft.getText());	
    	  double r = Double.parseDouble(jTFRight.getText());
    	  FajaManager.crear_Faja_Ancho_Fijo(l, r);
    	  Close();
    	 }
    	 else
    	  JOptionPane.showMessageDialog(null, "Faltan valores por llenar");	 
    	} 
    	else   //Faja de ancho legislativo
    	{		
   		 FajaManager.crear_Faja_Ancho_Legislacion();
         Close();
    	} 
    }

